# Drawing shapes using numpy
import cv2
import numpy as np
image = np.zeros((512,512,3))
cv2.imshow("Black Rectangle(color)",image)
cv2.waitKey(3000)
image_bw=np.zeros((512,512))
cv2.imshow("Black Rectangle(B&W)",image_bw)
cv2.waitKey(3000)
cv2.line(image,(0,0),(511,511),(255,127),5)
cv2.imshow("Blueline",image)
cv2.waitKey(3000)
image = np.zeros((512,512,3))
cv2.rectangle(image,(100,100),(300,250),(127,50,127),5)
cv2.imshow("rectangle",image)
cv2.waitKey(3000)
cv2.rectangle(image,(100,100),(300,250),(127,50,127),-1)
cv2.imshow("rectangle",image)
cv2.waitKey(3000)
cv2.circle(image,(350,350),100,(15,75,50),-1)
cv2.imshow("circle",image)
cv2.waitKey(3000)
pts=np.array([[10,50],[400,50],[90,200],[50,500]],np.int32)
pts=pts.reshape(-1,1,2)
cv2.polylines(image,[pts],True,(0,0,255),5)
cv2.imshow("polygon",image)
cv2.waitKey(5000)
cv2.destroyAllWindows()