import cv2
image1 = cv2.imread("/Users/rajaathota72/Desktop/Charlotteville-Trinidad-Tobago.jpg.image.750.563.low.jpg")
cv2.imshow("image1",image1)
cv2.waitKey(1000)
sobel_x=cv2.Sobel(image1,cv2.CV_64F,0,1,ksize=5)
cv2.imshow("sobel",sobel_x)
cv2.waitKey(1000)
sobel_y=cv2.Sobel(image1,cv2.CV_64F,1,0,ksize=5)
cv2.imshow("sobel",sobel_y)
cv2.waitKey(1000)
sobel_bw=cv2.bitwise_or(sobel_x,sobel_y)
cv2.imshow("sobel_bw",sobel_bw)
cv2.waitKey(1000)
laplacian=cv2.Laplacian(image1,cv2.CV_64F)
cv2.imshow("laplacian",laplacian)
cv2.waitKey(1000)
canny=cv2.Canny(image1,20,170)
cv2.imshow("canny",canny)
cv2.waitKey(9000)
cv2.destroyAllWindows()
